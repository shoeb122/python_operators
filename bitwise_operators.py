# Bitwise Operators in Python

a = 10  # 1010 in binary
b = 4   # 0100 in binary

print("Bitwise AND (a & b):", a & b)   # 0  (0000)
print("Bitwise OR (a | b):", a | b)    # 14 (1110)
print("Bitwise XOR (a ^ b):", a ^ b)   # 14 (1110)
print("Bitwise NOT (~a):", ~a)         # -11 (inverts all bits)
print("Left Shift (a << 2):", a << 2)  # 40 (101000)
print("Right Shift (a >> 2):", a >> 2) # 2  (0010)
